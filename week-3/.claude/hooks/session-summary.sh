#!/usr/bin/env bash
# Stop hook: Generate session summary and cost log entry.
#
# Matcher: *
# Writes session-summary.md with session stats.
# Appends a cost entry to costs.jsonl.

set -euo pipefail

AUDIT_DIR="$(cd "$(dirname "$0")/.." && pwd)/audit"
SUMMARY_FILE="${AUDIT_DIR}/session-summary.md"
COST_LOG="${AUDIT_DIR}/costs.jsonl"
BLOCKED_LOG="${AUDIT_DIR}/blocked.log"
mkdir -p "${AUDIT_DIR}"

TIMESTAMP=$(date -u +"%Y-%m-%dT%H:%M:%SZ")
BRANCH=$(git branch --show-current 2>/dev/null || echo "unknown")
TOKENS="${CLAUDE_TOKENS:-0}"

# Count files modified in this session
FILES_MODIFIED=$(git diff --name-only 2>/dev/null | wc -l | tr -d ' ')
FILES_STAGED=$(git diff --cached --name-only 2>/dev/null | wc -l | tr -d ' ')
TOTAL_FILES=$(( FILES_MODIFIED + FILES_STAGED ))

# Count blocked actions
BLOCKS=0
if [ -f "${BLOCKED_LOG}" ]; then
  BLOCKS=$(grep -c "BLOCKED" "${BLOCKED_LOG}" 2>/dev/null || echo "0")
fi

# Estimate cost (rough average ~$8/MTok for mixed input/output)
ESTIMATED_COST=$(python -c "print(f'{${TOKENS} * 0.000008:.4f}')" 2>/dev/null || echo "0.0000")

# Write session summary
cat > "${SUMMARY_FILE}" << EOF
# Session Summary

**Date:** ${TIMESTAMP}
**Branch:** ${BRANCH}
**Tokens used:** ${TOKENS}
**Estimated cost:** \$${ESTIMATED_COST}

## Activity
- Files modified: ${TOTAL_FILES}
- Blocks triggered: ${BLOCKS}

## Notes
Auto-generated by session-summary.sh stop hook.
EOF

# Append cost entry to costs.jsonl
python -c "
import json, sys
entry = {
    'timestamp': sys.argv[1],
    'tokens': int(sys.argv[2]),
    'estimated_cost_usd': sys.argv[3],
    'branch': sys.argv[4]
}
print(json.dumps(entry))
" "${TIMESTAMP}" "${TOKENS}" "${ESTIMATED_COST}" "${BRANCH}" \
  >> "${COST_LOG}"
